    public ArrayList<User> userList(){
        
        ArrayList<User> usersList = new ArrayList<>();
     
         
        try{
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/newspaperagency","root","");
            
            String q1="select * from distributor";
            
            stmt = (Statement) con.createStatement();   
            ResultSet rs= stmt.executeQuery(q1);
            User user;
            while(rs.next()){
                user=new User(rs.getInt("D_id"), rs.getString("Name"), rs.getString("Area"), rs.getInt("Contact"));
                usersList.add(user);
                
            }
           
        }
        catch(SQLException e){
              JOptionPane.showMessageDialog(null,e);
        
    }
      
        return usersList;
        
    }
    
    public void show_user(){
        
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel) DistributorTable.getModel();
        
        Object[] row = new Object[4];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getD_id();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getArea();
            row[3]=list.get(i).getContact();
            
            model.addRow(row);
            
        }
        
        
    }

