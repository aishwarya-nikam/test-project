    private void AddbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddbActionPerformed
       
        String D_id=Didfield.getText();
        String Dname=Namefield.getText();
        String Contact=Contactfield.getText();
        String Area=Areafield.getSelectedItem().toString();
      
        
        try{
            pstmt=con.prepareStatement("insert into distributor values(?,?,?,?)");
            pstmt.setString(1,D_id);
            pstmt.setString(2,Dname);
            pstmt.setString(3,Area);
            pstmt.setString(4,Contact);
            
            int i=pstmt.executeUpdate();
            
            DefaultTableModel model = (DefaultTableModel) DistributorTable.getModel();
              model.setRowCount(0);
              show_user();
        
            if(i>0){
                JOptionPane.showMessageDialog(null,"Distributor Added!!");
            }
            else{
                 JOptionPane.showMessageDialog(null,"Failed to add Distributor!");
                
                }
           }   
            
         catch (SQLException e) {
             JOptionPane.showMessageDialog(null,e);
         }
        
        
        
        
    }//GEN-LAST:event_AddbActionPerformed

